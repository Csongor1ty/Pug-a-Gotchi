public class Kaja {

    private int taplalas;
    private int ar;

    public Kaja(int taplalas, int ar) {
        this.taplalas = taplalas;
        this.ar = ar;
    }

    public int getTaplalas() {
        return taplalas;
    }

    public void setTaplalas(int taplalas) {
        this.taplalas = taplalas;
    }

    public int getAr() {
        return ar;
    }

    public void setAr(int ar) {
        this.ar = ar;
    }
}
