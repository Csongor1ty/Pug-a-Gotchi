public class Eletcsomag {

    private int arszabas;
    private int healeles;

    public Eletcsomag(int arszabas, int healeles) {
        this.arszabas = arszabas;
        this.healeles = healeles;
    }

    public int getArszabas() {
        return arszabas;
    }

    public void setArszabas(int arszabas) {
        this.arszabas = arszabas;
    }

    public int getHealeles() {
        return healeles;
    }

    public void setHealeles(int healeles) {
        this.healeles = healeles;
    }
}
